    ax1.set_ylabel("Vibración (g)", color='#00ffcc') # Etiqueta Y
    ax1.set_ylim(0, 2.0) # Escala fija de 0 a 2g
    ax1.axhline(LIMITE_VIBRACION, color='red', linestyle='--', alpha=0.5) # Línea punteada de límite
    ax1.legend(loc='upper left') # Leyenda
    ax1.grid(True, alpha=0.15) # Cuadrícula suave

    # Línea de Corriente (Naranja - Eje Gemelo)
    ax1_twin = ax1.twinx() # Creamos un segundo eje Y sobre el mismo gráfico
    linea_corr, = ax1_twin.plot(np.zeros(MAX_DATOS), color='#ffaa00', label='Corriente (A)', ls='--', lw=2)
    ax1_twin.set_ylabel("Corriente (A)", color='#ffaa00')
    ax1_twin.set_ylim(0, 1.5) # Escala fija de 0 a 1.5A
    ax1_twin.axhline(LIMITE_CORRIENTE, color='orange', linestyle='--', alpha=0.5) # Línea punteada límite
    ax1_twin.legend(loc='upper right')

    # --- GRÁFICO 2: FFT (Abajo Izquierda) ---
    ax2 = plt.subplot(2, 2, 3) # Ubicación: Fila 2, Columna 1
    ax2.set_facecolor('black')
    ax2.set_title("Espectro de Frecuencia (FFT)", color='white')
    ax2.set_xlabel("Hz"); ax2.set_ylabel("Mag"); ax2.set_ylim(0, 1.0)
