    # --- GRÁFICO 3: Scatter (Abajo Derecha) ---
    ax3 = plt.subplot(2, 2, 4) # Ubicación: Fila 2, Columna 2
    ax3.set_facecolor('black')
    ax3.set_title("Matriz Carga vs Vibración", color='white')
    ax3.set_xlabel("Amperios"); ax3.set_ylabel("Vibración g")
    ax3.set_xlim(0, 1.0); ax3.set_ylim(0, 2.0)
