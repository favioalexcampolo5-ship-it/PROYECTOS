def procesar_datos(nuevo_amp, nueva_vib):
    """ Guarda el dato nuevo y borra el más viejo (Cola FIFO) """
    historial_corriente.append(nuevo_amp)   # Agrega la corriente nueva al final de la lista
    historial_vibracion.append(nueva_vib)   # Agrega la vibración nueva al final de la lista

    # Si la lista creció más de lo permitido (60 datos)
    if len(historial_corriente) > MAX_DATOS:
        historial_corriente.pop(0) # Borramos el dato más antiguo (el primero)
        historial_vibracion.pop(0) # Borramos el dato más antiguo de vibración

    # Devolvemos las listas convertidas a Arrays de Numpy para poder graficarlas rápido
    return np.array(historial_corriente), np.array(historial_vibracion)

def calcular_fft_corregida(arr_vib, fs):
    """ Calcula las frecuencias de la vibración (Matemática compleja) """
    n = len(arr_vib) # Contamos cuántos datos tenemos

    # Si hay muy pocos datos o todo es cero, no calculamos nada
    if n < 10 or np.max(arr_vib) == 0:
        return np.linspace(0, 10, 10), np.zeros(10)

    ventana = np.hanning(n) # Filtro suavizado para evitar errores de borde
    espectro = np.fft.fft(arr_vib * ventana) # Aplicamos la Transformada Rápida de Fourier
    freqs = np.fft.fftfreq(n, d=1/fs) # Calculamos el eje X (Hertz)
    mag = (np.abs(espectro) / n) * 2  # Calculamos la altura de las barras (Magnitud real)

    mitad = n // 2 # Solo nos sirve la mitad positiva del espectro
    return freqs[:mitad], mag[:mitad] # Devolvemos frecuencia y magnitud


