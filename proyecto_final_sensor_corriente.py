def inicializar_dashboard():
    """ Dibuja el fondo, títulos y ejes UNA SOLA VEZ al principio """
    plt.style.use('dark_background') # Ponemos tema oscuro estilo hacker
    fig = plt.figure(figsize=(12, 10), facecolor='black') # Creamos el lienzo negro
    # Ajustamos márgenes para que todo quepa bien
    plt.subplots_adjust(top=0.77, bottom=0.20, hspace=0.45, wspace=0.3)

    # --- Títulos Estáticos (No cambian) ---
    fig.text(0.5, 0.95, "PANEL DE MANTENIMIENTO PREDICTIVO - GRUPO 1", ha='center', fontsize=24, color='cyan', weight='bold')
    fig.text(0.5, 0.90, "ANÁLISIS DE CAUSA RAÍZ Y DIAGNÓSTICO", ha='center', fontsize=14, color='white')
    fig.text(0.5, 0.80, "Monitor de Tendencia en Tiempo Real", ha='center', fontsize=12, color='gray')

    # --- Objetos Dinámicos (Textos que cambiarán) ---
    # Creamos el cuadro del KPI vacío por ahora
    ref_kpi = fig.text(0.5, 0.85, "CONECTANDO...", ha='center', fontsize=16, color='white',
                       bbox=dict(facecolor='#222', edgecolor='white', boxstyle='round'))

    # Creamos los espacios para los mensajes de alerta (Estado, Causa, Acción)
    ref_icono = fig.text(0.5, 0.14, "", ha='center', fontsize=20, weight='bold')
    ref_causa = fig.text(0.5, 0.09, "", ha='center', fontsize=14, color='yellow')
    ref_accion = fig.text(0.5, 0.04, "", ha='center', fontsize=13, color='white', weight='bold')

    # --- GRÁFICO 1: Líneas (Arriba) ---
