import time     # Librería para controlar pausas y tiempos
import requests # Librería para conectarse a internet
import numpy as np  # Librería para matemáticas avanzadas y arrays
import matplotlib.pyplot as plt # Librería para crear los gráficos
from IPython.display import clear_output, display # Para actualizar el gráfico sin parpadear

# --- CONFIGURACIÓN GLOBAL ---
MI_DISPOSITIVO = "proy_final_utp_funciona_v1" # Nombre del script
URL_API = f"https://dweet.cc/get/latest/dweet/for/{MI_DISPOSITIVO}" # Dirección web de donde se guardan los datos
HEADERS = {'User-Agent': 'Mozilla/5.0'} # Nos identificamos como un navegador para que no nos bloqueen

# Parámetros (Límites de seguridad)
LIMITE_VIBRACION = 1.15 # Si pasa de esto, es peligroso (Línea Roja en gráfico)
LIMITE_CORRIENTE = 0.70 # Si pasa de esto, el motor sufre (Línea Naranja en gráfico)
MAX_DATOS = 60          # Cantidad de puntos que se ven en la pantalla (el ancho del gráfico)
FS_ESTIMADA = 20        # Velocidad estimada de datos (para calcular la frecuencia FFT)

# Buffers (Listas de memoria)
# Creamos listas llenas de ceros para empezar con la gráfica vacía
historial_vibracion = [0.0] * MAX_DATOS
historial_corriente = [0.0] * MAX_DATOS

# --- FUNCIONES ---

def obtener_datos_estable():
    """ Descarga los datos de la nube de forma segura """
    try:
        # Intenta conectarse a Dweet con un tiempo máximo de espera de 2 segundos
        respuesta = requests.get(URL_API, headers=HEADERS, timeout=2.0)

        # Si la conexión fue exitosa (Código 200 significa OK)
        if respuesta.status_code == 200:
            datos = respuesta.json() # Convertimos la respuesta de texto a unidades por bloques, como una estructura

            # Verificamos si realmente llegaron datos dentro del paquete
            if "with" in datos and len(datos["with"]) > 0:
                contenido = datos["with"][0]["content"] # Sacamos el contenido útil

                # Extraemos y devolvemos los 3 valores clave, poniendo 0.0 si falta alguno
                return (float(contenido.get("amp", 0.0)),
                        float(contenido.get("vib", 0.0)),
                        contenido.get("status", "DESCONECTADO"))
    except:
        pass # Si falla el internet, no hacemos nada (evitamos que el programa se cierre)
    return None, None, None # Si algo falló, devolvemos "vacío"


