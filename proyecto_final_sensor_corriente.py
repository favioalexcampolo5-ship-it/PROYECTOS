def actualizar_dashboard(refs, arr_a, arr_v, val_a, val_v, estado):
    """ Actualiza SOLO los datos, colores y textos """

    # 1. Actualizar números del KPI central
    refs['kpi'].set_text(f"VIB: {val_v:.2f} g  |  AMP: {val_a:.2f} A")
    # --- SEMÁFORO DE COLORES ---
    # Definimos estado "Normal" por defecto (Negro y Verde)
    bg, txt, ico, msj, cau, acc = "black", "#00ff00", "✅", "NORMAL", "Estable", "Monitoreo activo"

    # Si hay PARADA MANUAL (Prioridad Morada)
    if "MANUAL" in estado:
        bg, txt, ico = "#220022", "magenta", "🖐️"
        msj = "PARADA MANUAL"
        cau, acc = "Operador Local (Pulsador)", "Verificar antes de reiniciar"

    # Si hay STOP DE EMERGENCIA (Prioridad Roja)
    elif "STOP" in estado:
        bg, txt, ico = "#330000", "#ff3333", "⛔"
        msj = "PARADA DE EMERGENCIA"
        # Determinamos si fue por Corriente o Vibración para dar el mensaje correcto
        if "AMP" in estado or "SOBRECORRIENTE" in estado:
            cau, acc = f"Sobrecarga detectada ({val_a} A)", "REVISAR OBSTRUCCIÓN MECÁNICA"
        else:
            cau, acc = f"Desbalance Crítico ({val_v} g)", "REVISAR RODAMIENTOS / EJE"

    # Si hay ALERTA PREVENTIVA (Prioridad Naranja)
    elif "ALERTA" in estado:
        bg, txt, ico = "#442200", "orange", "⚠️"
        msj = "ALERTA TÉCNICA"
        cau, acc = "Umbrales excedidos", "Programar mantenimiento preventivo"

    # Si hay RESET (Prioridad Verde)
    elif "RESET" in estado:
        bg, txt, ico = "black", "#00ff00", "🔄"
        msj = "SISTEMA REARMADO / EN MARCHA"
        cau, acc = "Reset por Operador", "Retomando monitoreo..."

    # Aplicamos los colores y textos calculados al gráfico
    refs['fig'].set_facecolor(bg) # Cambia color de fondo
    refs['ico'].set_text(f"{ico} {msj}"); refs['ico'].set_color(txt)
    refs['ico'].set_bbox(dict(facecolor='black', edgecolor=txt, boxstyle='round,pad=0.5'))
    refs['cau'].set_text(cau)
    refs['acc'].set_text(acc)

    # 2. Actualizar las Líneas del gráfico principal
    x_data = range(len(arr_v)) # Creamos eje X de 0 a 60
    refs['l_vib'].set_data(x_data, arr_v) # Actualizamos línea vibración
    refs['l_cor'].set_data(x_data, arr_a) # Actualizamos línea corriente

    # 3. Actualizar Gráfico FFT
    f, m = calcular_fft_corregida(arr_v, FS_ESTIMADA) # Calculamos matemática nueva
    refs['ax2'].cla() # Limpiamos barras viejas
    refs['ax2'].set_title("Espectro de Frecuencia (FFT)", color='white')
    # Ajustamos altura del eje Y dinámicamente
    ymax = max(0.5, np.max(m)*1.2) if len(m)>0 else 0.5
    refs['ax2'].set_ylim(0, ymax)
    # Dibujamos barras nuevas en MORADO
    refs['ax2'].bar(f, m, width=0.5, color='#bf00ff', alpha=0.9)
    refs['ax2'].grid(True, alpha=0.2)

    # 4. Actualizar Gráfico de Dispersión
    refs['ax3'].cla() # Limpiamos puntos viejos
    refs['ax3'].set_title("Matriz Carga vs Vibración", color='white')
    refs['ax3'].set_xlim(0, 1.0); refs['ax3'].set_ylim(0, 2.0)
    # Repintamos las zonas de peligro
    refs['ax3'].axhspan(LIMITE_VIBRACION, 2.0, color='red', alpha=0.1)
    refs['ax3'].axvspan(LIMITE_CORRIENTE, 1.0, color='orange', alpha=0.1)

    # Calculamos color de cada punto (Rojo si peligro, Verde si bien)
    colores = ['red' if (v > LIMITE_VIBRACION or a > LIMITE_CORRIENTE) else 'lime' for a, v in zip(arr_a, arr_v)]
    refs['ax3'].scatter(arr_a, arr_v, c=colores, s=40) # Dibujamos puntos
    refs['ax3'].set_xlabel("Amperios"); refs['ax3'].set_ylabel("Vibración g")
    refs['ax3'].grid(True, alpha=0.2)

    return refs['fig'] # Devolvemos la figura actualizada

# --- BUCLE PRINCIPAL (DONDE TODO EMPIEZA) ---
print("Iniciando Dashboard Final (Rearme sin borrado)...")

# 1. Creamos el tablero una sola vez antes de empezar
referencias = inicializar_dashboard()
handle = display(referencias['fig'], display_id='tablero_final_rearme') # Lo mostramos en pantalla

try:
    while True: # Bucle infinito (corre para siempre)
        # Bajamos datos de internet
        d_amp, d_vib, d_estado = obtener_datos_estable()

        if d_amp is not None: # Si llegaron datos válidos...
            # Procesamos y guardamos en historial
            arr_a, arr_v = procesar_datos(d_amp, d_vib)

            # Actualizamos los dibujos en pantalla
            actualizar_dashboard(referencias, arr_a, arr_v, d_amp, d_vib, d_estado)

            # Refrescamos la imagen
            handle.update(referencias['fig'])

            # Esperamos 0.5 segundos para no saturar la red (Estabilidad)
            time.sleep(0.5)
        else:
            # Si falló internet, esperamos igual
            time.sleep(0.5)

except KeyboardInterrupt: # Si presionamos stop en el teclado
    print("Sistema detenido.")
    plt.close('all') # Cierra todas las ventanas gráficas